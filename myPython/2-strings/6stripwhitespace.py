#right whitespace
name="sunyog "
print(name)
print(name.rstrip())
name=name.rstrip()
print(name)

#left white space
name=" sunyog"
print(name)
print(name.lstrip())
name=name.lstrip()
print(name)

#left and right white strip at the same time
name=" sunyog "
print(name)
print(name.strip())
name=name.strip()
print(name)